#Arithmetic Operations

num_1 = 10
num_2 = 7

# + for addition
sum = num_1  +  num_2

# - for subtraction
difference = num_1 - num_2

# * for multiplication
product = num_1 * num_2

# / for division
quotient = num_1 / num_2

# for floor division
floor_division = num_1 // num_2

# % for modulus (returns the remainder)
modulus = num_1 % num_2

# ** for exponentiation
exponentiation = num_1 % num_2

print(f'sum of {num_1} and {num_2}: {sum}, difference of {num_1} and {num_2}: {difference}, product of {num_1} and {num_2}: {product}, quotient of {num_1} and {num_2}: {quotient}, modulus of {num_1} and {num_2}: {modulus}, exponentiation of {num_1} and {num_2}: {exponentiation}')