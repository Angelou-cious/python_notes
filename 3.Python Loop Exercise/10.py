# Define the upper limit for our number sequence
numbers = 10

# Loop through numbers from 1 to 'numbers' (inclusive)
# range(1, numbers + 1) generates sequence: 1, 2, 3, ..., 10
for number in range(1, numbers + 1):
    # Print each number in the sequence
    print(number)

# Print completion message after the loop ends
print(f'Done!')