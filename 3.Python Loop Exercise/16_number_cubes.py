input_number = 6 # initialize the number

for number in range(1, input_number + 1): # start the loop from 1 to input_number + 1 to include
                                          # the last number
    
    cube = number ** 3 # get the cube using the exponentiation(raising a number to a power)

    print(f'Current Number is : {number}  and the cube is {cube}') # in each iteration print the
                                                                   # cube