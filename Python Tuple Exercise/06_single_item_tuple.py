"""
    Exercise 6: Create a tuple with single item 50
"""

# Create a tuple containing a single integer element 50
# Note: The trailing comma after 50 is ESSENTIAL for creating a single-item tuple
# Without the comma, (50) would just be an integer in parentheses, not a tuple
tuple1 = (50,)

# Print the tuple to the console to display its contents
# This will output: (50,) showing it's a tuple with one element
print(tuple1)
